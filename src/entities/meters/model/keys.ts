export const metersKeys = {
  all: ["meters"] as const,
  list: (
    page: number,
    limit: number,
    search: string,
    isArchived: boolean,
    companyId: string,
    controllerId: string,
    locationType: string,
    accountNumber: string,
    clientName: string,
    address: string,
    isValveLockedByManager: string,
  ) =>
    [
      ...metersKeys.all,
      page,
      limit,
      search,
      isArchived,
      companyId,
      controllerId,
      locationType,
      accountNumber,
      clientName,
      address,
      isValveLockedByManager,
    ] as const,
  details: (id: string) => [...metersKeys.all, "details", id] as const,
};
